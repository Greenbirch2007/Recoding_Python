#　建立由列表组成的列表

#　有时候会需要初始化一个嵌套着几个列表的列表，比如一个列表可能需要用来存放的不同的学生名单，最好的选择是使用列表推导

# 示例2-12  一个包含3个列表的列表,嵌套的3个列表各自有3个元素来代表第一个狂

board = [['_']*3 for i in range(3)]
print(board)
board[1][2] = 99
print(board)

